package switchcase;


public class score {
	public static void main (String[] args) {
	char score='B';
	switch(score) {
	case 'A':
		System.out.println("A IS BETWEEN 90 AND 100");
	break;
	case 'I':
		System.out.println("B IS BETWEEN 80 AND 90");
		break;
	case 'C':
		System.out.println("C is between 70 and 80");
		break;
	case 'D':
		System.out.println("D is betwenn 60 and 70");
		default:
			System.out.println("Fail");

	
	}
	
	}
}
