package switchcase;

public class sc {
	public static void main(String[] args) {
		int a=5;
		switch(a)
		{
		case 1:
			System.out.println("case 1 is 5");
			break;
		case 5:
			System.out.println("case2 is 6");
			break;
		case 7:
			System.out.println("case7 is 7");
			default:
			System.out.println("case is not exsist");
		}
		
		
		
	}

}
